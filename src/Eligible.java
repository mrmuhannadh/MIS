    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
            try {
                pst= conn.prepareStatement("SELECT st_id,st_name,course,sex FROM records where st_id=?");
            } catch (SQLException ex) {
                Logger.getLogger(Eligible.class.getName()).log(Level.SEVERE, null, ex);
            }
            int st_id=Integer.parseInt(stid.getText());
        try {
            pst.setInt(1,st_id);
        } catch (SQLException ex) {
            Logger.getLogger(Eligible.class.getName()).log(Level.SEVERE, null, ex);
        }
            ResultSetLec rsl = null;
        try {
            rsl = (ResultSetLec) pst.executeQuery();
        } catch (SQLException ex) {
            Logger.getLogger(Eligible.class.getName()).log(Level.SEVERE, null, ex);
        }
            if(rsl.next()==false)
                {
                JOptionPane.showMessageDialog(this,"Record not found");
                sname.setText("");
                course.setText("");
                sex.setText("");
                stid.requestFocus();
            }
            else{
                sname.setText("st_name");
                course.setText("course");
                sex.setText("sex");
            }
        
    }//GEN-LAST:event_jButton2ActionPerformed

