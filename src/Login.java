    try{
        Class.forName("com.mysql.jdbc.Driver");
        Connection con= DriverManager.getConnection("jdbc:mysql://localhost:3308/mis","root","");
        String query="select * from users where user_id=? and password=?";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, name.getText());
        pst.setString(2, pW.getText());
        ResultSet rs = pst.executeQuery();
        String user=name.getText();
        
        if(rs.next()){
                String post = rs.getString("post");
                switch(post){
                case "Admin":
                    new admin_window().setVisible(true);
                    this.setVisible(false);
                           
                    break;
                case "Student":
                    new student_window(user).setVisible(true);
                    this.setVisible(false);
                    break;  
                case "Lecture":
                    new Lecture_Window().setVisible(true);
                    this.setVisible(false);
                    break;      
                case "Technical_Officer":
                    new TO_Window(user).setVisible(true);
                    this.setVisible(false);
                    break;
            }
        }else{
            JOptionPane.showMessageDialog(null, "not");
        }
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
    }

        
