    public void show_marks(){
        ArrayList<Marks>list=ul.marksList();
        DefaultTableModel model=(DefaultTableModel) marks.getModel();
        Object[] row=new Object[16];
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getst_id();
            row[1]=list.get(i).getlec_id();
            row[2]=list.get(i).getquiz_1();
            row[3]=list.get(i).getquiz_2();
            row[4]=list.get(i).getquiz_3();
            row[5]=list.get(i).getquiz_4();
            row[6]=list.get(i).getassessment_1();
            row[7]=list.get(i).getassessment_2();
            row[8]=list.get(i).getassessment_3();
            row[9]=list.get(i).getmid();
            row[10]=list.get(i).getfinal_theory(); 
            row[11]=list.get(i).getfinal_practical(); 
            row[12]=list.get(i).getTotal();
            row[13]=list.get(i).getGrade_point();
            row[14]=list.get(i).getGrade();
            row[15]=list.get(i).getsubid();
            model.addRow(row);
            
        }
        
        
    }
